void reaumur(celcius){
  double reaumur = celcius * 4/5;
  print('Reaumur = $celcius * 4/5 = $reaumur');
}

void fahrenheit(celcius){
  double fahrenheit = celcius * 9/5 + 32;
  print('Fahrenheit = $celcius * 9/5 + 32 = $fahrenheit');
}

void kelvin(celcius){
  double kelvin = celcius + 273.15;
  print('Kelvin = $celcius * 273.15 = $kelvin');
}